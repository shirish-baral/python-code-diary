import numpy as np

# np.sort()
np1 = np.array([3,2,4,5,6,1,0,9,8,7])
print(np.sort(np1))

# Alphabetical
np2 = np.array(["Shirish", "Bruno", "Pogba", "Tywin"])
print(np2)
print(np.sort(np2))

# Booleans T/F
np3 = np.array([True, False, True, True])
print(np.sort(np3))

# 2-D
np4 = np.array([[6,7,1,9],[8,3,5,0]])
print(np4)
print(np.sort(np4))